; registers global:
;   B - configuration switches
;   C - phase (0x00/0xFF)

init:
    ld sp, 0x3fff
    ld	a, 0x98
    out	(_p03_set), a
    ld	a, 0x80
    out	(_p07_set), a
    ld	a, 0x80
    out	(_p0b_set), a
    ld	a, 0x92
    out	(_p1d_set), a
    ld	a, 0x16
    out	(_p3b_set), a
    ld	a, 0x56
    out	(_p3b_set), a
    ld	a, 0x96
    out	(_p3b_set), a
    ld	a, 0x10
    out	(_p0a_mem_man), a
    ld	a, 0x00
    out	(_pf8_vmode), a
	;jp		fill_vmem

main:
    in      a, (_p00_config)
    ld      b, a        ; save configuration to B
    jp      set_turbo
main_turbo:
    ld      a, TEST_VBANK_IDX
    jp      set_vbank   ; set video bank to 0 (0xC000-0xFFFF)
main_vbank:
    bit     CONFIG_MEM_TEST_BIT, b
    jr      NZ, mem_test
main_memt:
    ld      a, b        ; load configuretion to A
    and     0x07        ; extract video test number
    ld      h, 0
    ld      l, a
    add     hl, hl
    ld      de, video_tests
    add     hl, de
    ld      a, (hl)
    inc     hl
    ld      h, (hl)
    ld      l, a
    jp      (hl)
main_video_end:
	ld		de, TEST_TITLE
	ld		iyh, 0
	ld		iyl, 28
	ld		hl, main_title_end
	jp		print_str
main_title_end:
    ; wait loop
    ld      d, 0
wait_loop0:
	inc		d
    ld      hl, 0x0000
wait_loop1:
	inc		hl
	ld		a, h
	sub		a, 0x80
    jr      C, wait_loop1
	ld		a, d
	sub		a, 4
    jr      C, wait_loop0
    jr      main

TEST_TITLE:
	.db "ORION-128 NG tests (andreil, 2020)", 0x00

mem_test:
    ; monochrome video mode
    ld      a, VMODE_2COLOR_1PALLETE
    out     (_pf8_vmode), a
    ; disable widescreen
    ld      a, TEST_VBANK_IDX
    out     (_pfa_vbank), a
    ; enable turbo mode
    in      a, (_p0a_mem_man)
    set     MEMM_TURBO_BIT, a
    out     (_p0a_mem_man), a
mem_test_main_loop:
    ; draw screen
	ld		de, TEST_TITLE
	ld		iyh, 0
	ld		iyl, 31
	ld		hl, main_title_end
	jp		print_str
    jr      mem_test_main_loop

