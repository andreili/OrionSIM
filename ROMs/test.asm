fill_vmem:
	; enable window on RAM1
	in		a, (_p0a_mem_man)
	set		MEMM_RAM1EN_BIT, a
	out		(_p0a_mem_man), a
	ld		c, 0
	; set RAM 1 segment
loop_seg:
	ld		a, c
	out		(_p05_RAM1), a
	; screen pointer
	ld		hl, RAM1_WINDOW
	ld		d, 00
loop_vm:
    ld      (hl), d
    inc     hl
	; check end
	ld		a, h
	cp		0x80
    jr      NZ, loop_vm
	ld		a, c
	inc		c
	cp		28
    jr      NZ, loop_seg
	; disable window on RAM1
	in		a, (_p0a_mem_man)
	res		MEMM_RAM1EN_BIT, a
	nop
	out		(_p0a_mem_man), a
	jp main

set_turbo:
    in      a, (_p0a_mem_man)
    res     MEMM_TURBO_BIT, a
    bit     CONFIG_TURBO_BIT, b
    jr      Z, turbo_not_set
    set     MEMM_TURBO_BIT, a
turbo_not_set:
    out     (_p0a_mem_man), a
    jp      main_turbo

set_vbank:
    bit     CONFIG_WIDE_BIT, b
    jr      NZ, wide_not_set
    set     VB_WIDE_BIT, a
wide_not_set:
    out     (_pfa_vbank), a
    jp      main_vbank

