print_chr:
; parameters:
;  A - char
;  iyh - column
;  iyl - row
;  ix - return address
; usage:
;  shadow registers
	exx
	; get character start
    ld      de, font
	ld		h, 0
	ld		l, a
	add		hl, hl	; *2
	add		hl, hl	; *2
	add		hl, hl	; *2
	add		hl, de
	; calculate screen start
	ld		a, iyl ;28
	add		a, a	; *2
	add		a, a	; *2
	add		a, a	; *2
	ld		e, a
	ld		a, iyh ;0
	add		a, 0xc0
	ld		d, a
	; set count
	ld		bc, 0x0008
	ldir
	exx
	jp		ix

